package chapter5;

public class SwitchEx2 {

	public static void main(String[] args) {
		int number = 1;
		
		switch (number) {
		case 1:
			System.out.println("1번");
			break;
		case 2:
			System.out.println("2번");
			break;
		case 3:
			System.out.println("3번");
			break;
			
		default:
			break;
		}
	}

}
